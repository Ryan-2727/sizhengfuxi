function relatedFacts(courseId, title, facts, index) {
  const normalizedTitle = title.replace(/\s/g, "");
  const hits = facts.filter((fact) => normalizedTitle.includes(fact[0]) || fact.some((part) => normalizedTitle.includes(part.slice(0, 4))));
  const start = (index * 4) % Math.max(facts.length, 1);
  const windowFacts = facts.slice(start, start + 16).concat(facts.slice(0, Math.max(0, start + 16 - facts.length)));
  return uniqueFactList(hits.concat(windowFacts)).slice(0, 12);
}

function uniqueFactList(items) {
  const seen = new Set();
  return items.filter((item) => {
    const key = item[0];
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function uniqueWords(words) {
  const cleaned = words
    .map((word) => String(word).replace(/[。；;,.，、]/g, "").trim())
    .filter((word) => word.length >= 2 && word.length <= 18);
  return [...new Set(cleaned)];
}

function formatHighlight(value) {
  return escapeHtml(value)
    .replace(/\*\*(.+?)\*\*/g, (_, text) => shouldHighlightTerm(text) ? `<mark class="hl">${text}</mark>` : text)
    .replace(/!!(.+?)!!/g, (_, text) => shouldUseRed(text) ? `<span class="red-em">${text}</span>` : text);
}

function shouldHighlightTerm(text) {
  const plainLabels = [
    "教材位置", "理论背景", "核心事件", "制度安排", "现实要求", "答题场景",
    "背景/条件", "核心观点", "意义/作用", "易错点", "大题模板",
    "主线句", "是什么", "为什么", "怎么做或历史意义"
  ];
  if (plainLabels.includes(text)) return false;
  if (text.length > 28 && !/[《》]/.test(text)) return false;
  return /《|》|会议|条约|制度|章程|纲领|路线|思想|理论|运动|起义|战争|革命|改造|改革开放|中国共产党|中华人民共和国|社会主义|马克思主义|毛泽东|邓小平|科学发展观|三个代表|习近平|新时代|中国式现代化|一国两制|五位一体|四个全面|新发展理念|共同富裕|全过程人民民主|总体国家安全观|人类命运共同体|宪法|法治|核心价值观/.test(text);
}

function shouldUseRed(text) {
  return /意义|作用|影响|标志|推动|促进|奠定|实现|开辟|决定|保证|提高|增强|巩固|确立|形成|发展|转折|胜利|地位|价值|贡献|要求|必须|关键|根本/.test(text);
}

