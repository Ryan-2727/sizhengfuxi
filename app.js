function buildChapterDetail(course, chapter, index) {
  if (course.id === "history") return buildHistoryPdfChapterDetail(course, chapter, index);
  if (course.id === "morality") return buildMoralityPdfChapterDetail(course, chapter, index);
  const facts = supplements[course.id]?.facts || [];
  const related = relatedFacts(course.id, chapter[0], facts, index);
  const courseFrame = chapterFrame(course.id);
  const keywords = uniqueWords([
    ...chapter[0].replace(/[第章节绪论导论\s]/g, " ").split(/[、，：:（）()“”]/),
    ...related.flatMap((fact) => [fact[0], fact[1]])
  ]).slice(0, 10);
  const paragraphs = [
    `本章复习要先抓住“**${chapter[0]}**”的核心问题：${chapter[1]} 复习时不要只背结论，要能说明该结论出现的!!历史条件、理论依据和现实指向!!。`,
    `围绕新版教材和公开复习资料的主线，可把本章拆成“**概念界定、理论逻辑、重点判断、答题表达**”四层。选择题重点看关键词和易混选项，大题重点用规范表述解释!!原因、意义和方法!!。`,
    `背诵时建议先记住本章的“**主线句**”：${courseFrame.mainLine} 再把主线句拆成若干知识点，形成从概念到论述题的答题链条。`,
    `本章最容易失分的地方，是只写口号式关键词而没有解释。考试作答应至少写出“**是什么**”“**为什么**”“**怎么做或历史意义**”三层内容，并把!!核心概念!!放在每段开头。`
  ];
  const sentences = related.slice(0, 6).map((fact) => `**${fact[0]}**：${fact[1]}，!!${fact[2]}!!。`);
  while (sentences.length < 6) {
    const point = course.keypoints[(index + sentences.length) % course.keypoints.length];
    sentences.push(`**${point}**。`);
  }
  const content = related.slice(0, 8).map((fact, factIndex) => {
    const verbs = ["掌握", "理解", "区分", "能够表述", "联系题目分析", "形成答题框架", "辨析易错说法", "归纳材料题角度"];
    return `${verbs[factIndex % verbs.length]}“**${fact[0]}**”：核心是“${fact[1]}”，答题时必须补充“!!${fact[2]}!!”。`;
  });
  content.push(
    `按“**背景/条件**”整理：说明本章理论或事件出现的时代背景、社会基础、实践需要，避免直接跳到结论。`,
    `按“**核心观点**”整理：把教材中的定义、判断、总路线、基本原则或方法论写成可背诵短句。`,
    `按“**意义/作用**”整理：回答其历史意义、理论意义、现实意义，尤其注意!!教材常用规范表述!!。`,
    `按“**易错点**”整理：把相近概念放在一起比较，例如原因与意义、目标与路径、总布局与战略布局、原理与方法论。`,
    `按“**大题模板**”整理：第一段点题，第二至四段分点展开，最后一段回扣课程主线和现实要求。`
  );
  while (content.length < 12) {
    const point = course.keypoints[(index + content.length) % course.keypoints.length];
    content.push(`围绕“**${point}**”整理选择题易错点和大题关键词。`);
  }
  return {
    paragraphs,
    sentences,
    keywords,
    content,
    reviewCards: buildReviewCards(course, chapter, related, index),
    citation: buildChapterCitation(course, chapter, related),
    examSections: buildExamSections(course, chapter, related, index),
    combined: buildCombinedChapterContent(paragraphs, sentences, content),
    groupedSections: buildExamSections(course, chapter, related, index),
    advice: buildExamAdvice(course.id, related),
    map: {
      root: chapter[0],
      branches: [
        { title: "概念", items: related.slice(0, 2).map((fact) => fact[0]) },
        { title: "判断", items: related.slice(2, 4).map((fact) => fact[1]) },
        { title: "答题", items: ["背景", "原因", "意义", "方法", "现实要求"] },
        { title: "易错", items: keywords.slice(0, 4) }
      ]
    }
  };
}

