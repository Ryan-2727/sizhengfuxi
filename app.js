function chapterPriority(courseId, title, index) {
  const highKeywords = {
    history: ["共产党", "抗日", "新时代", "辛亥", "五四", "改革开放"],
    morality: ["理想", "核心价值观", "法治", "道德"],
    mao: ["新民主主义", "社会主义改造", "邓小平", "科学发展观", "毛泽东思想"],
    xi: ["中国式现代化", "党的全面领导", "高质量发展", "全过程人民民主", "全面从严治党"],
    marx: ["物质性", "实践与认识", "人类社会", "资本主义", "共产主义"]
  };
  const hit = (highKeywords[courseId] || []).some((word) => title.includes(word));
  if (hit) return { level: "S", reason: "属于高频主线章节，选择题和大题都容易围绕它设置。" };
  if (index <= 2) return { level: "A", reason: "属于课程基础章节，后续章节经常调用这里的概念。" };
  if (index <= 6) return { level: "B", reason: "属于中频章节，重点掌握关键词和易混判断。" };
  return { level: "C", reason: "属于补充巩固章节，考前用来查漏补缺。" };
}

